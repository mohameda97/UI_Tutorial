package com.example.uitutorial;

public class FavouriteContactList {
    private String name;
    private String imageUrl;


    public FavouriteContactList(String name, String imageUrl) {
        this.name = name;
        this.imageUrl = imageUrl;

    }

    public String getName() {
        return name;
    }

    public String getImageUrl() {
        return imageUrl;
    }


}
